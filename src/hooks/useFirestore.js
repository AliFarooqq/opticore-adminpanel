import { useState, useEffect, useCallback } from 'react';

/**
 * Generic hook for loading Firestore data with loading/error states.
 * @param {() => Promise<any[]>} fetchFn - async function that returns an array
 * @param {any[]} deps - dependency array (re-fetches when these change)
 */
export function useFirestoreCollection(fetchFn, deps = []) {
  const [data, setData] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  const load = useCallback(async () => {
    setLoading(true);
    setError(null);
    try {
      const result = await fetchFn();
      setData(result);
    } catch (err) {
      setError(err.message || 'Failed to load data');
    } finally {
      setLoading(false);
    }
  }, deps); // eslint-disable-line react-hooks/exhaustive-deps

  useEffect(() => {
    load();
  }, [load]);

  return { data, loading, error, reload: load };
}

